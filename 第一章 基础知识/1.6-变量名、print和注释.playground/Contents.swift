import UIKit

//字符串使用双引号
var str = "Hello, playground"

//类型名一般首字母大写,具体的变量一般小写
let imInt    :Int      = 3
let imFloat  :Float    = 3.14
let imDouble :Double   = 3.1415926
let imCGFloat:CGFloat  = 9.0
let imBool   :Bool     = true
let imString :String   = "Hello,swift:)"

//swift的变量名可以是任意字符
var 名字 = "小明"
print(名字)
var 😀 = "smile"
print(名字+😀)

//print可以一次性打印
let x=1,y=2,z=3,b=true
print(x,y,z,b)
//可以使用separator指定分隔符,默认是空格
//使用terminator指定尾部的终止符，默认是\n
print( x , y , z ,separator:"--",terminator:":)")
print("hello")

//在print里面使用\()可以直接打印变量
print(y , "*" , z ,"=",y*z)
print("\(y) * \(z) = \(y*z)")


